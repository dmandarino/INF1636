package modelos;

public class Casa {

	private Integer numCasa;
	private Peca peca;
	private Integer x;
	private Integer y;
	private Boolean isBranca; 
	
	public Integer getNumCasa() {
		return numCasa;
	}
	public void setNumCasa(Integer numCasa) {
		this.numCasa = numCasa;
	}
	public Peca getPeca() {
		return peca;
	}
	public void setPeca(Peca peca) {
		this.peca = peca;
	}
	public Integer getX() {
		return x;
	}
	public void setX(Integer x) {
		this.x = x;
	}
	public Integer getY() {
		return y;
	}
	public void setY(Integer y) {
		this.y = y;
	}
	public Boolean getIsBranca() {
		return isBranca;
	}
	public void setIsBranca(Boolean isBranca) {
		this.isBranca = isBranca;
	}
	
}
